const url = new URL(window.location.href);
const searchParams = url.searchParams;
const id = searchParams.get('id') || 1;

function renderIntroduce(page) {
    const introduce = page.introduce.map(item => `<p>${item}</p>`).join('');
    return `
        <div class="main__body-introduce">
            ${introduce}
        </div>`;
}

function rendercontent(page) {
    let items = ""; 

    page.body.forEach((item) => {
        let contenttext = ""; 
        let content = ""; 

        if ('content' in item) { 
            item.content.forEach((contentp) => {
                contenttext += `<p>${contentp}</p>`;
            });

            content = `
                <div class="body__items-content">
                    ${contenttext}                    
                </div>`;
        }

        items += ` 
            <div class="main__body-items">
                <div class="body__items-image">
                    <div class="body__items-img">
                        <img src="${item.src}" alt="">
                    </div>
                    <p class="body__items-node">${item.alt}</p>
                </div>
                ${content}
            </div>`;
    });

    return items; 
}

function renderPage(id) {
    let page = arrpage.find(item => item.id == id);
    if (!page) return; 

    let introduce = renderIntroduce(page);
    let content = rendercontent(page);
    
    let html = `
        <div class="section6__main-header d-flex flex-column gap-2">
            <div class="main__header-item">${page.item}</div>
            <div class="main__header-title">${page.title}</div>
            <div class="main__header-author">${page.author}</div>
            <div class="main__header-tags">
                <div class="d-inline-block ps-2 pe-2 rounded-pill">
                    <i class="fa-solid fa-tags"></i> ${page.tags}
                </div>
            </div>
            <div class="main__header-time">${page.time}</div>
        </div>
        <div class="section6__main-body">
            ${introduce}
            ${content}
        </div>`;

    document.querySelector('.section6__body-main').innerHTML = html;
}

renderPage(id);